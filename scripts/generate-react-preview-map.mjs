import { existsSync, mkdirSync, writeFileSync, readFileSync } from "node:fs";
import { dirname, join } from "node:path";
import fg from "fast-glob";

const REACT_ROOT = "registry/react";
const OUTPUT_PATH = "src/generated/react-preview-map.tsx";

// A preview entry exists for every react/<category>/<slug>/ folder that ships
// component.tsx. If preview.tsx is present it wins (demo wrapper); otherwise
// the component's own default export is rendered.
const componentFiles = (
  await fg(`${REACT_ROOT}/*/*/component.tsx`, { onlyFiles: true })
).sort();

const entries = componentFiles.map((filePath) => {
  const componentDir = dirname(filePath);
  const [category, slug] = componentDir.split("/").slice(-2);
  const hasPreview = existsSync(join(componentDir, "preview.tsx"));
  const moduleSpecifier = `@registry/react/${category}/${slug}/${hasPreview ? "preview" : "component"}`;

  return { key: `${category}/${slug}`, moduleSpecifier };
});

const mapBody = entries
  .map(
    ({ key, moduleSpecifier }) =>
      `  "${key}": dynamic(() => import("${moduleSpecifier}"), { ssr: false, loading: PreviewLoading }),`,
  )
  .join("\n");

const output = `// AUTO-GENERATED by scripts/generate-react-preview-map.mjs — do not edit by hand.
// Regenerate with: npm run generate:react-previews
"use client";

import dynamic from "next/dynamic";
import type { ComponentType } from "react";

function PreviewLoading() {
  return (
    <div className="flex h-full min-h-[360px] items-center justify-center text-sm text-white/50">
      Loading preview…
    </div>
  );
}

export const reactPreviewMap: Record<string, ComponentType> = {
${mapBody}
};
`;

mkdirSync(dirname(OUTPUT_PATH), { recursive: true });

const previous = existsSync(OUTPUT_PATH) ? readFileSync(OUTPUT_PATH, "utf8") : null;
if (previous !== output) {
  writeFileSync(OUTPUT_PATH, output);
}

console.log(`Generated ${OUTPUT_PATH} with ${entries.length} React preview ${entries.length === 1 ? "entry" : "entries"}.`);
